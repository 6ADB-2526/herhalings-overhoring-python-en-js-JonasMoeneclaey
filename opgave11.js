function rekenmachine(getal1, getal2, bewerking) {
    if (bewerking == "+") {
        console.log(getal1 + getal2);
    }
    if (bewerking == "-") {
        console.log(getal1 - getal2)
    }
    if (bewerking == "*") {
        console.log(getal1*getal2);
        
    }
    if (bewerking == "/") {
        console.log(getal1/getal2);
        
    }
}

rekenmachine(3,3,"*")
